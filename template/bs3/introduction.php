<meta http-equiv="Content-Type" content="text/html; charset=utf-8" /><span class="lemmaTitleH1">ACM<span>（国际大学生程序设计竞赛）</span></span></h1>         <div class="para">ACM-Association for Computing Machinery , 即美国计算机协会。</div><div class="para">ICPC-International Collegiate Programming Contest , 即国际大学生程序设计竞赛。</div><div class="para">ACM国际大学生程序设计竞赛(英文全称：ACM International Collegiate Programming Contest（ACM-ICPC或ICPC）是由美国计算机协会（ACM）主办的，一项旨在展示大学生创新能力、团队精神和在压力下编写程序、分析和解决问题能力的年度竞赛。经过近30多年的发展，ACM国际大学生程序设计竞赛已经发展成为最具影响力的大学生计算机竞赛。赛事目前由IBM公司赞助。</div>

<div><span class="biTitle">中&nbsp;&nbsp;文&nbsp;&nbsp;名：ACM国际大学生程序设计竞赛</span></div>

<div><span >外&nbsp;&nbsp;文&nbsp;&nbsp;名：ACM International Collegiate Programming Contest</span></div>

<div>简&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;称：ACM-ICPC或ICPC</span></div>

<div>主办单位：美国计算机协会（ACM）</span></div>

<h4 ><B><font color=blue>1.发展历史</font></B></h4><div class="para">竞赛的历史可以上溯到1970年，当时在美国德克萨斯A&amp;M大学举办了首届比赛。当时的主办方是the Alpha Chapter of the UPE Computer Science HonorSociety。作为一种全新的发现和培养计算机科学顶尖学生的方式，竞赛很快得到美国和加拿大各大学的积极响应。1977年，在ACM计算机科学会议期间举办了首次总决赛，并演变成为目前的一年一届的多国参与的国际性比赛。到2015年1月已经举办了37届最初几届比赛的参赛队伍主要来自美国和加拿大，后来逐渐发展成为一项世界范围内的竞赛。特别是自1997年IBM开始赞助赛事之后，赛事规模增长迅速。1997年，总共有来自560所大学的840支队伍参加比赛。而到了2004年，这一数字迅速增加到840所大学的4109支队伍并以每年10-20%的速度在增长。</div>

<div class="para">1980年，ACM将竞赛的总部设在位于美国德克萨斯州的贝勒大学</a>。</div>

<div class="para">在赛事的早期，冠军多为美国和加拿大的大学获得。而进入1990年代后期以来，俄罗斯和其它一些东欧国家的大学连夺数次冠军。来自中国大陆的上海交通大学代表队则在2002年美国夏威夷的第26届和2005年上海的第29届,2010年哈尔滨的34届全球总决赛上三夺冠军。这也是目前为止亚洲大学在该竞赛上取得的最好成绩。来自中国大陆的浙江大学代表队在2011年在美国佛罗里达州的奥兰多的35届全球总决赛上夺冠。赛事的竞争格局已经由最初的北美大学一枝独秀演变成目前的亚欧对抗的局面。</div>

<div class="para">ACM-ICPC以团队的形式代表各学校参赛，每队由3名队员组成。每位队员必须是在校学生，有一定的年龄限制，并且最多可以参加2次全球总决赛和5次区域选拔赛。</div>

<div class="para">比赛期间，每队使用1台电脑需要在5个小时内使用C、C++或Java中的一种编写程序解决7到10个问题。程序完成之后提交裁判运行，运行的结果会判定为正确或错误两种并及时通知参赛队。而且有趣的是每队在正确完成一题后，组织者将在其位置上升起一只代表该题颜色的气球。</div>

<div class="para">最后的获胜者为正确解答题目最多且总用时最少的队伍。每道试题用时将从竞赛开始到试题解答被判定为正确为止，其间每一次提交运行结果被判错误的话将被加罚20秒时间，未正确解答的试题不记时。例如：A、B两队都正确完成两道题目，其中A队提交这两题的时间分别是比赛开始后1:00和2:45，B队为1:20和2:00，但B队有一题提交了2次。这样A队的总用时为1:00+2:45=3:45而B队为1:20+2:00+0:20=3:40，所以B队以总用时少而获胜。</div>



<h4 ><B><font color=blue>2.竞赛流程</font></B></h4><div class="para">1.参赛队伍最多由三名参赛队员组成。</div>

<div class="para">2.竞赛中一般命题10题左右，试题描述为英文，比赛时间为5个小时，前四个小时可以看到实时排名，最后一小时封榜，无法看到排名。</div>

<div class="para">3.竞赛可以使用的语言：C++、C、Java和Pascal。但final赛只有C/C++;</div>

<div class="para">4.重点考察选手的算法和程序设计能力，不考察任何Windows编程知识；</div>

<div class="para">5.选手可携带任何非电子类资料，包括书籍和打印出来的程序等，部分赛区会对携带的资料进行限制；</div>

<div class="para">6.评委负责将结果（正确或出错的类型）通过网络尽快返回给选手，除此之外不提供任何额外帮助；</div>

<h4 ><B><font color=blue>3.决赛</font></B></h4><div class="para">赛事由各大洲区域预赛和全球总决赛两个阶段组成。各预赛区第一名自动获得参加全球总决赛的资格。决赛安排在每年的3-4月举行，而区域预赛一般安排在上一年的9-12月举行。一个大学可以有多支队伍参加区域预赛，但只能有一支队伍参加全球总决赛</div>

<div class="para">全球总决赛第一名将获得奖杯一座。另外，成绩靠前的参赛队伍也将获得金、银和铜牌。而解题数在中等以下的队伍会得到确认但不会进行排名。</div>

<h4 ><B><font color=blue>4.评分办法</font></B></h4><div class="para">竞赛进行5个小时，一般有6—8道试题，由同队的三名选手使用同一台计算机协作完成。当解决了一道试题之后，将其提交给评委，由评委判断其是否正确。若提交的程序运行不正确，则该程序将被退回给参赛队，参赛队可以进行修改后再一次提交该问题。</div>

<div class="para">竞赛结束后，参赛各队以解出问题的多少进行排名，若解出问题数相同，按照总用时的长短排名。总用时为每个解决了的问题所用时间之和。一个解决了的问题所用的时间是竞赛开始到提交被接受的时间加上该问题的罚时(每次提交通不过，罚时20分钟)。没有解决的问题不记时。美国英语为竞赛的工作语言。竞赛的所有书面材料(包括试题)将用美国英语写出，区域竞赛中可以使用其它语言。总决赛可以使用的程序设计语言包括pascal，c，c++及java，也可以使用其它语言。具体的操作系统及语言版本各年有所不同。</div>

<h4 ><B><font color=blue>5.奖励情况</font></B></h4><div class="para">区域赛一般分别按10%，20%，30%的比例颁发金，银，铜奖，即一般情况（120队伍）有12支队伍获金牌，24支队伍获银牌，36支队伍获铜牌，其余为优胜奖。</div>

<div class="para">2010年亚洲区域赛中国大陆5大赛区由阿里巴巴公司赞助，获得金牌的选手可以享受绿色通道，进入阿里巴巴实习或参加工作。</div>

<div class="para">2012年亚洲区域赛中国大陆5大赛区由华为公司赞助，有奖金(冠5000、亚3000、季2000，非冠亚季的金牌1200，FB（First Blood，指全场第一个解答出某道题） 800，最佳女队800，顽强拼搏500)。顽强拼搏是全场最后一个AC，且只AC1道题的队伍。</div>

<div class="para">总决赛前十名的队伍将得到高额奖学金：第一名奖金为12000美元，第二名奖金为6000美元，第三名奖金为3000美元，第四名至第十名将各得到1500美元。除此之外还将承认北美冠军、欧洲冠军、南太平洋冠军及亚洲冠军。</div>

